    <strong>
        <h3>Como funciona o Deep Learning?</h2>
    </strong>
    <p>
     A técnica de inteligência artificial compreende que o aprendizado da máquina deve ser semelhante ao do ser humano, ou seja, através de exemplos. Da mesma forma que aprendemos a identificar o que é um gato e o que é um cachorro, por exemplo, as máquinas devem receber informações que as possibilitem diferenciar um gato de um cachorro em um processo de aprendizagem semelhante ao do ser humano. Vale lembrar que, da mesma forma que nós cometemos erros durante a aprendizagem, as máquinas também estão suscetíveis a erros.
