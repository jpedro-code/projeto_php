<div>
    <strong>
        <h3>Qual a diferença entre Machine Learning e Deep Learning?</h2>
    </strong>
    <ul>
      <li>Em Machine Learning, as imagens são apresentadas para as máquinas e as características que diferenciam e reconhecem os objetos precisam ser informadas manualmente. O profissional cria então um modelo capaz de descrever ou prever o objeto a partir dos dados apresentados – as características de cada objeto.</li>
      <li>Já em Deep Learning, não é necessário que o profissional informe as características de cada objeto. As imagens são diretamente inseridas no algoritmo, que busca semelhanças e aprende com os exemplos.</li>
