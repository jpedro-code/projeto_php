    <strong>
        <h3>O que é Deep Learning?</h2>
    </strong>
    <p>
      O deep learning é uma rede neural com múltiplas camadas que assimilam tarefas e reconhecem símbolos a partir de dados. Ou seja, a deep learning treina computadores para realizar tarefas como seres humanos, o processo é realizado por parâmetros básicos sobre os dados, e assim a maquina aprende sozinha através de reconhecimentos de padrões em várias camadas de processamento.
    </p>
</div>
