    <strong>
        <h2>O que é Machine Learning?</h2>
    </strong>
    <p>
    Machine Learning é uma técnica de análise de dados que ensina os computadores a fazer o que naturalmente os seres humanos e aos animais já fazem: aprender com a experiência. Os algoritmos de machine learning ou aprendizagem de máquinas utilizam métodos computacionais para “aprender” a informação diretamente dos dados sem depender de uma equação pré-determinada como modelo. Os algoritmos melhoram o seu desempenho à medida que o número de amostras disponíveis para aprendizagem aumenta. Já o Deep Learning é uma forma especializada de machine learning.
    </p>
</div>
<div>
    <strong>
        <h2>Para que serve Machine Learning?</h2>
    </strong>
    <h4>Com o crescimento do Big Data, Machine Learning se tornou uma técnica-chave para a solução de problemas em diversas áreas como:</h4>
    <ul>
        <li>Computação financeira, para análise de crédito e algoritmos de comercialização</li>
        <li>Processamento de imagens e visão computacional, para reconhecimento facial, detecção de movimento e detecção de objetos</li>
        <li>Biologia Computacional, para detecção de tumores, desenvolvimento de medicamentos e sequenciamento genético</li>
        <li>Produção de energia, para precificação e previsão de carga de energia</li>
        <li>Automotiva, aeroespacial e manufatura, para manutenção preditiva</li>
        <li>Processamento de linguagem natural, para aplicações de reconhecimento de voz</li>
    </ul>
</div>
<div>
    <strong><h2>Como funciona a machine learning</h2></strong>
    <p>Algoritmos de machine learning encontram padrões nos dados que geram insights que nos ajudam a tomar melhores decisões e fazer predições mais assertivas.  Os algoritmos auxiliam em diagnósticos médicos, mercado de ações, previsão da carga de energia, entre outras coisas. Machine learning é o que está por trás de sites e aplicativos que fazem sugestões e recomendações de conteúdos como filmes e músicas, mesma técnica utilizada por lojas para obter insights do comportamento de seus clientes.</p>
</div>
<div>
    <strong><h2>Quando devo usar machine learning?</h2></strong>
    <p>Considere utilizar machine learning quando você se deparar com problemas complexos que envolvem uma grande quantidade de dados e muitas variáveis, mas nenhuma fórmula ou equação existente. Machine Learning é uma excelente opção se você precisa lidar com situações como:</p>
    <ul>
        <li>Quando equações e regras são muito complexas – como em reconhecimento facial e reconhecimento de voz, por exemplo;</li>
        <li>Quando as regras de uma tarefa estão em constante mudança – como em casos de detecção de fraudes</li>
        <li>Quando a natureza dos dados mudam constantemente e o programa precisa de adaptações – com em casos de comercializações automatizadas, previsão de demanda de energia e predições de tendências de consumo</li>
    </ul>
</div>
